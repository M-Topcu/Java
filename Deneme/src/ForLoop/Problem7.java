package ForLoop;

public class Problem7 {
    // verilen 2 harf ve aralarindaki harfleri yazdiran bir kod yaziniz

    public static void main(String[] args) {

        char ilkHarf = 'A';
        char sonHarf = 'N';

        for (char i = ilkHarf; i <= sonHarf; i++) {
            System.out.print(i + " ");

        }
    }
}
